package com.camp.project1;

public class Contact {
    private String name;
    private String number;

    public void setName(String Name){
        this.name = Name;
    }
    public void setNumber(String Number){
        this.number = Number;
    }

    public String getName(){
        return name;
    }

    public String getNumber(){
        return number;
    }
}
